char* itoa(int val, int base) {
	static char buf[32] = {0};
	int i = 30;
	for(; val && i ; --i, val /= base)
		buf[i] = "0123456789abcdef"[val % base];
	return &buf[i+1];
}

int main()
{
    char* msg;
    msg = itoa(12345, 10);
    printf("%s", msg);
    return 0;
}
